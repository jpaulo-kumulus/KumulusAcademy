count = 0
while count != 5:
    count += 1
    print(count)

#  operador `+=` pega o valor atual de count, adiciona 1 e atribui o novo valor de volta a count

count = 0
while count <= 20:
    count += 3
    print(count)
# pode ser mais do q apenas 1

count = 20
while count >= 0:
    count -= 3
    print(count)
# o `-=` faz a mesma coisa so que subtraindo

# =	    Atribuir
# +=	Adicionar e, em seguida, atribuir
# -=	Subtrair e, em seguida, atribuir
# *=	Multiplicar e, em seguida, atribuir
# /=	Dividir e, em seguida, atribuir
# %=	Obter o módulo e, em seguida, atribuir
# **=	Executar o expoente e, em seguida, atribuir
# //=	Executar a divisão de base e, em seguida, atribuir
